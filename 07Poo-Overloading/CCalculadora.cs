namespace Overloading;

public class CCalculadora{
  public int Suma(int a, int b){
    int r = 0;
    r = a + b;
    return r;
  }
  public double Suma(double a, double b){
    double r = 0;
    r = a + b;
    return r;
  }
  public int Suma(int a, int b, int c){
    int r = 0;
    r = a + b;
    return r;
  }
}